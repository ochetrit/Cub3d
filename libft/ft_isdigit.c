int	ft_isdigit(int c)
{
	if (c < 48 || c > 57)
	{
		return (0);
	}
	return (1);
}

int	ft_isspace(int c)
{
	if (c == ' ' || (c < 14 && c > 7))
		return (1);
	return (0);
}
